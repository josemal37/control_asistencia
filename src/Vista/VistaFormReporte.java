    private void guardarArchivo() {
        try {
            int mes = Integer.parseInt(this.jTextFieldMes.getText());
            int anio = Integer.parseInt(this.jTextFieldAnio.getText());

            JFileChooser selectorArchivo = new JFileChooser();
            int seleccion = selectorArchivo.showSaveDialog(controladorReporte.getjDialogReporte());

            if (seleccion == JFileChooser.APPROVE_OPTION) {
                this.controladorReporte.GuardarReporte(mes, anio, selectorArchivo.getSelectedFile());
                controladorReporte.cerrar();
            }
        } catch (Exception ex) {
            VistaMensajes.mostrarMensaje("Los datos introducidos no son correctos.");
        }
    }

